	 MOV R4,#00H	;CLEAR R4 TO STORE CARRY	 
 	 MOV A,#0FFH	;A=FFH
 	 ADD A,#01H		;A=FF+O1H=00H,CY=1
 	 MOV R2,A		;R2=00H
	 MOV A,#0FFH	;A=FFH
	 ADDC A,#10H	;A=FF+10H=00H,CY=1
	 MOV R3,A		;R3=1OH
	 JNC NEXT		;JUMP TO NEXT OF CARRY NOT EQUAL TO 1
	 INC R4			;INCREMENT R4
NEXT:SJMP $
	 END