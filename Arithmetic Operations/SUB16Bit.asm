;Subtracting 2 16 bit Numbers 
MOV R4,#00H	;CLEAR R4 TO STORE CARRY 
CLR C	;CLEAR CARRY FLAG MOV 
A,#22H	;A=22H 
SUBB A,#77H	;A=22H-77H=ABH,B=1 
MOV R2,A	;R2=ABH 
MOV A,#22H	;A=22H 
SUBB A,#77H	;A=22H-O1H-77H=AAH,B=1 
MOV R3,A	;R3=AAH 
MOV A,#00H	;CLEAR A TO STORE BORROW A=00H 
SUBB A,00H	;A=A-0-1=0-0-1=FFH 
MOV R4,A	;R4=FFH 
SJMP $ 
END	